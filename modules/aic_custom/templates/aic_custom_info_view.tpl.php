    </div>

    <div class="w3-container">
      <p>On every digital page you will find a toolbar along the top right. You can increase and decrease the size of the type by clicking the <strong>type size increase</strong> and <strong>decrease icons</strong>. Click the <strong>printer icon</strong> to generate a printer-friendly version of the entry. Click the <strong>user icon</strong> to register for an account or log on to an existing account. The <strong>search box</strong> at far right allows you to search throughout the entire catalogue.</p>
