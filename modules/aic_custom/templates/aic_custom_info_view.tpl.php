    <div class="w3-container w3-padding-8">
      <h3><strong>Tools to help you study the images</strong></h3>
    </div>

    <div class="w3-container">
      <p>Each artwork in the catalogue is accompanied by high-resolution images, and a number of tools facilitate their study. Click the thumbnail of the artwork at the top left corner to activate the <strong>navigator window</strong>; this allows you to zoom into an artwork with a reminder of the overall image. Use the vertical slider bar to zoom in and out. The four-arrow <strong>full-screen icon</strong> immediately expands the image to the full size of the digital page; clicking on the image when at full-page size returns it to its normal size, as does the circle-shaped <strong>refresh icon</strong> to the left. For interactive images, click the jagged-line <strong>annotations icon</strong> and <strong>layer icon</strong> to see the available conservation annotations and technical images; the horizontal slider allows you to compare any two images. For three-dimensional objects, click and drag the object with your cursor or use the <strong>360&deg; viewer icon</strong> at top right (see following page). </p>
